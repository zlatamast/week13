let a = prompt(`Какое будет первое число?`, `Напиши сюда число`);

let b = prompt(`Какое будет второе число?`, `Напиши сюда число`);

function calcSum() {
    alert( +a + +b );
};

function calcSubt() {
    alert( +a - +b );
};

function calcMult() {
    alert( +a * +b );
};

function calcDiv() {
    alert( +a / +b );
};

